class Deer{
	String name;
	int age;
	String mbti;
	String gender;


	public String getName(){
		return name;
	}
	public int getAge(){
		return age;
	}
	public String getMBTI(){
		return mbti;
	}
	public String getGender(){
		return gender;
	}

	public Deer(String n, int a, String m, String g){
		name = n;
		age = a;
		mbti = m;
		gender = g;

	}

}